package F_function_methods;

public class swap {
    public static void main(String[] args) {
        int a =12 ;
        int b =23 ;

        // swap a number by call a value
        int temp = a;
        a = b ;
        b = temp ;

        System.out.println("a = "+ a);
        System.out.println("b = "+ b);
    }
}
